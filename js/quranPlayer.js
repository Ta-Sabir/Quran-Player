  PlayerImg = document.querySelector(".playerImg");
let quranIndex = 0;

let quranNotPlay = false;

window.addEventListener("load", () => {
  quranLoad(quranIndex);
});
function quranLoad(index) {
  playerImg.src = allQuranAudio[index].image;
  quranName.innerHTML = allQuranAudio[index].name;
  theReader.innerHTML = allQuranAudio[index].theReader;
  quranAudio.src = allQuranAudio[index].src;
}
// quranPlay
function quranPlay() {
  quranNotPlay = true;
  playerPlay.querySelector("i").innerHTML = "pause";
  PlayerImg.classList.add("play");
  quranAudio.play();
}
// quranpause
function quranpause() {
  quranNotPlay = false;
  playerPlay.querySelector("i").innerHTML = "play_arrow";
  PlayerImg.classList.remove("play");
  quranAudio.pause();
}
playerPlay.addEventListener("click", () => {
  quranNotPlay ? quranpause() : quranPlay();
});
// nextQuran
function nextQuran() {
  quranIndex++;
  quranIndex > allQuranAudio.length - 1
    ? (quranIndex = 0)
    : (quranIndex = quranIndex);
  quranLoad(quranIndex);
  quranPlay();
}
next.addEventListener("click", () => {
  nextQuran();
});
// prevQuran
function prevQuran() {
  quranIndex--;
  quranIndex < 0
    ? (quranIndex = allQuranAudio.length - 1)
    : (quranIndex = quranIndex);
  quranLoad(quranIndex);
  quranPlay();
}
prev.addEventListener("click", () => {
  prevQuran();
});
repeat.addEventListener("click", () => {
  let getText = repeat.innerHTML;
  switch (getText) {
    case "repeat":
      repeat.innerHTML = "repeat_one";
      break;

    case "repeat_one":
      repeat.innerHTML = "repeat";
      break;
  }
});
quranAudio.addEventListener("ended", () => {
  let getText = repeat.innerHTML;
  switch (getText) {
    case "repeat":
      nextQuran();
      break;
    case "repeat_one":
      quranAudio.currentTime = 0;
      quranLoad(quranIndex);
      quranAudio.play();
      break;
  }
});
quranAudio.addEventListener("timeupdate", (event) => {
  let currentTime = event.target.currentTime;
  let durationTime = event.target.duration;
  let quranTimeWidth = (currentTime / durationTime) * 100;
  progressBar.style.width = `${quranTimeWidth}%`;
  quranAudio.addEventListener("loadeddata", () => {
    const interval = setInterval(() => {
      let current = quranAudio.currentTime;
      startTime.innerHTML = formatTime(current);
    }, 1000);
    let durationT = quranAudio.duration;
    maxTime.innerHTML = formatTime(durationT);
    quranAudio.addEventListener("ended", () => {
      clearInterval(interval);
    });
  });
});
playerProgress.addEventListener("click", (e) => {
  let progressBarWidth = playerProgress.clientWidth;
  let progressBarX = e.offsetX;
  let quraduration = quranAudio.duration;
  quranAudio.currentTime =
    quraduration - (progressBarX / progressBarWidth) * quraduration;
  quranAudio.play();
});
function formatTime(time) {
  if (time && !isNaN(time)) {
    const munites =
      Math.floor(time / 60) < 10
        ? `0${Math.floor(time / 60)}`
        : Math.floor(time / 60);
    const seconds =
      Math.floor(time % 60) < 10
        ? `0${Math.floor(time % 60)}`
        : Math.floor(time % 60);
    return `${munites}:${seconds}`;
  }
  return "00:00";
}
